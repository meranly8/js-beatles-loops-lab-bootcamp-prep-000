function theBeatlesPlay(musicians,instruments) {
  const array = []
  
  for (let i = 0; i < musicians.length; i++ ){
    var musician = musicians[i]
    var instrument = instruments[i]
    
    array.push(`${musician} plays ${instrument}`)
  }
  return array
}

